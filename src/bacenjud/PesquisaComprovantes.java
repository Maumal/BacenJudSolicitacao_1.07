package bacenjud;

/**
 *
 * @author f7057419
 */
public class PesquisaComprovantes {
    String id;
    String pso;
    String data;
    String valor;
    String correio;

    public PesquisaComprovantes() {
    }

    
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPso() {
        return pso;
    }

    public void setPso(String pso) {
        this.pso = pso;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getCorreio() {
        return correio;
    }

    public void setCorreio(String correio) {
        this.correio = correio;
    }
    
    
    
    
}
